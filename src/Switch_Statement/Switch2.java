package Switch_Statement;

public class Switch2 {

	public static void main(String[] args) {

		char ch = 'P';
		
		switch(ch) {
		
		case 'D':System.out.println("Match A");
		case 'E':System.out.println("Match E");
		case 'M':System.out.println("Match MP");
		case 'P':System.out.println("Match P");
		break;
		default: System.out.println("No match");
	}
	}
}
